#learn about variable and datatypes

a = 1
print(" the type of a is ", type(a)) #type of a is int

b = "kapoor"
print(" the type of b is ", type(b))
#type of b is str

c = 1.0
print(" the type of c is ", type(c))
#type of c is float

d = True
print(" the type of d is ", type(d))
#type of d is bool

e = 1+2j
print(" the type of e is ", type(e))
#type of e is complex

f = None
print(" the type of f is ", type(f))
#type of f is NoneType

g = [1,2,3,4]
print(" the type of g is ", type(g))
#type of g is list

h = (1,2,3,4)
print(" the type of h is ", type(h))
#type of h is tuple

i = {1,2,3,4}
print(" the type of i is ", type(i))
#type of i is set

j = {"name":"kapoor","age":21}
print(" the type of j is ", type(j))
#type of j is dict
 